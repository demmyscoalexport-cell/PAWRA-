#!/usr/bin/env node
/**
 * Print env vars to mirror in Shopify Admin → Hydrogen → Environment variables.
 *   npm run oxygen:env-checklist
 *
 * Also writes a local copy-paste file (gitignored):
 *   oxygen-env-to-mirror.env
 */

import {writeFileSync} from 'node:fs';
import {resolve, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';
import {loadEnvFile} from './loadEnv.js';

loadEnvFile();

const KEYS = [
  'SESSION_SECRET',
  'PUBLIC_STOREFRONT_API_TOKEN',
  'PUBLIC_STORE_DOMAIN',
  'PUBLIC_STOREFRONT_ID',
  'PRIVATE_STOREFRONT_API_TOKEN',
  'PUBLIC_CHECKOUT_DOMAIN',
  'PUBLIC_CUSTOMER_ACCOUNT_API_CLIENT_ID',
  'PUBLIC_CUSTOMER_ACCOUNT_API_URL',
  'SHOPIFY_WEBHOOK_SECRET',
  'SHOPIFY_ADMIN_API_TOKEN',
  'PUBLIC_CLOUDINARY_CLOUD_NAME',
  'CLOUDINARY_URL',
  'WAVESPEED_API_KEY',
  'PUBLIC_JUDGEME_SHOP_DOMAIN',
  'JUDGEME_API_TOKEN',
  'PUBLIC_JUDGEME_PUBLIC_TOKEN',
  'PUBLIC_KLAVIYO_COMPANY_ID',
  'PUBLIC_GA4_MEASUREMENT_ID',
  'PUBLIC_SWYM_STORE_ID',
  'PUBLIC_SWYM_WISHLIST_URL',
  'PUBLIC_SMILE_PUBLISHABLE_KEY',
  'PUBLIC_SMILE_REWARDS_URL',
  'PUBLIC_GORGIAS_WIDGET_ID',
  'PUBLIC_LOOP_RETURNS_URL',
  'PUBLIC_RECHARGE_STORE_IDENTIFIER',
];

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outFile = resolve(root, 'oxygen-env-to-mirror.env');

console.log('PAWRA Oxygen environment checklist\n');
console.log('Paste into: Shopify Admin → Sales channels → Hydrogen → Environments → Production\n');

const lines = [
  '# Generated by npm run oxygen:env-checklist — DO NOT COMMIT',
  '# Shopify Admin → Hydrogen → Environments → Production → Environment variables',
  '',
];

let setCount = 0;
let emptyCount = 0;

for (const key of KEYS) {
  const value = process.env[key];
  const status = value ? '✓ set' : '○ empty';
  console.log(`${status.padEnd(8)} ${key}`);
  if (value) {
    setCount += 1;
    lines.push(`${key}=${value}`);
  } else {
    emptyCount += 1;
    lines.push(`# ${key}=`);
  }
}

writeFileSync(outFile, `${lines.join('\n')}\n`, 'utf8');

console.log(`\nWrote ${setCount} set values → ${outFile}`);
console.log(`${emptyCount} empty keys skipped (fill after installing apps).`);
console.log('\nStill required from you:');
console.log('  1. GitHub secret SHOPIFY_HYDROGEN_DEPLOYMENT_TOKEN (Oxygen deploy CI is failing)');
console.log('  2. PRIVATE_STOREFRONT_API_TOKEN from Hydrogen/Headless → Storefront API');
console.log('  3. Regenerate SHOPIFY_ADMIN_API_TOKEN if npm run probe:admin fails');
console.log('  4. Paste oxygen-env-to-mirror.env values into Oxygen production env');
